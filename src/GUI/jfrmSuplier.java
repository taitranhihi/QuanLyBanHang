    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String searchText = txt_Timkiem.getText().trim();

        // Kiểm tra xem chuỗi tìm kiếm có rỗng không
        if (searchText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ khóa tìm kiếm!");
            return;
        }
        List<Supplier> suppliers = SupplierDAO.timKiemNhaCungCap(searchText);

        // Hiển thị kết quả tìm kiếm trên bảng
        DefaultTableModel model = (DefaultTableModel) tbl_ListTT.getModel();
        model.setRowCount(0);
        for (Supplier supplier : suppliers) {
            model.addRow(new Object[]{
                supplier.getIdSupplier(),
                supplier.getName(),
                supplier.getPhone(),
                supplier.getAddress(),
                supplier.getEmail()
            });
        }                 
    }//GEN-LAST:event_btnTimKiemActionPerformed

