    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SupplierDAO supplierDAO = new SupplierDAO();
        String tenNCC = txt_tenNCC.getText().trim();
        String sdtNCC = txt_sdtNCC.getText().trim();
        String dcNCC = txt_dcNCC.getText().trim();
        String emailNCC = txt_EmailNCC.getText().trim();

        if (tenNCC.isEmpty() || sdtNCC.isEmpty() || dcNCC.isEmpty() || emailNCC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin nhà cung cấp!");
            return;
        }

            boolean themThanhCong = supplierDAO.themNhaCungCap(tenNCC, sdtNCC, dcNCC, emailNCC);

            // Kiểm tra kết quả và xử lý tương ứng
            if (themThanhCong) {
                txt_idNCC.setText(" ");
                txt_tenNCC.setText("");
                txt_sdtNCC.setText("");
                txt_dcNCC.setText("");
                txt_EmailNCC.setText("");
                LoadForm();
                JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thất bại!");
            }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
         String idNCC = txt_idNCC.getText().trim();

    if (idNCC.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nhà cung cấp để xóa!");
        return;
    }

    // Hiển thị hộp thoại xác nhận YES/NO
    int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa nhà cung cấp này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
    if (choice == JOptionPane.YES_OPTION) {
        boolean xoaThanhCong = SupplierDAO.xoaNhaCungCap(idNCC);
        if (xoaThanhCong) {
            JOptionPane.showMessageDialog(this, "Xóa nhà cung cấp thành công!");
            LoadForm();
            txt_idNCC.setText(" ");
            txt_tenNCC.setText(" ");
            txt_sdtNCC.setText(" ");
            txt_dcNCC.setText(" ");
            txt_EmailNCC.setText(" ");
        } else {
            JOptionPane.showMessageDialog(this, "Xóa nhà cung cấp thất bại!");
        }
    } else {
        // Hiển thị thông báo hủy xóa
        JOptionPane.showMessageDialog(this, "Hủy xóa nhà cung cấp.");
    }
    }//GEN-LAST:event_btnXoaActionPerformed

