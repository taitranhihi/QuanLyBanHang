public boolean validateData(){
    
    String username = txtUsernameRe.getText();
    String name = txtName.getText();
    String email= txtEmail.getText();
    String birthYear = txtBirthYear.getText();
    String address = txtAddress.getText();
    String phone = txtPhone.getText();
    if(username.isEmpty()||name.isEmpty()||email.isEmpty()||birthYear.isEmpty()||address.isEmpty()){
        return false;
    }
    if(!NhanVienDAO.kiemTraUserName(username)){
        erro.setText("Username đã tồn tại");
        return false;
    }
    if(username.contains(" ")){
        erro.setText("Username không hợp lệ");
        return false;
    }
    if(birthYear.matches("^\\\\d+$")){
        erro.setText("Năm sinh không hợp lệ");
        return false;
    }
    if(!NhanVienDAO.kiemTraEmail(email)){
        erro.setText("Email đã tồn tại");
        return false;
    }
    return true;
}
