    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateData()){    
            String userName = txtUsernameRe.getText();
            String passWord = txtPWRe.getText();
            String name = txtName.getText();
            String email = txtEmail.getText();
            String position = jcboPosition.getSelectedItem().toString();
            String phone = txtPhone.getText();
            String namsinh = txtBirthYear.getText();
            String address = txtAddress.getText();
            if(position.equals("Staff")){
                if(NhanVienDAO.CreateStaff(userName, passWord, name, email, position, address, namsinh, phone)){
                    JOptionPane.showMessageDialog(null, "Thêm thành công nhân viên "+name);
                }
            }
            else{
                if(NhanVienDAO.CreateManager(userName, passWord, name, email, position, address, namsinh, phone)){
                    JOptionPane.showMessageDialog(null, "Thêm thành công nhân viên "+name);
                }
            }
          
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

