public class Loai {
    int idcategory;
    String name;

    public Loai() {
    }

    public Loai(int idcategory, String name) {
        this.idcategory = idcategory;
        this.name = name;
    }

    public int getIdcategory() {
        return idcategory;
    }

    public void setIdcategory(int idcategory) {
        this.idcategory = idcategory;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

 
   
    
}
